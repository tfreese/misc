package de.freese.ga.gene;

/**
 * @author Thomas Freese
 */
public class ByteGene extends AbstractGene<Byte>
{
    /**
     * Erzeugt eine neue Instanz von {@link ByteGene}.
     */
    public ByteGene()
    {
        super();
    }

    /**
     * Erzeugt eine neue Instanz von {@link ByteGene}.
     *
     * @param value Byte
     */
    public ByteGene(final Byte value)
    {
        super();

        setValue(value);
    }
}

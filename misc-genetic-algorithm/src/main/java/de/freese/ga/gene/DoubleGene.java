package de.freese.ga.gene;

/**
 * @author Thomas Freese
 */
public class DoubleGene extends AbstractGene<Double>
{
    /**
     * Erzeugt eine neue Instanz von {@link DoubleGene}.
     */
    public DoubleGene()
    {
        super();
    }

    /**
     * Erzeugt eine neue Instanz von {@link DoubleGene}.
     *
     * @param value Double
     */
    public DoubleGene(final Double value)
    {
        super();

        setValue(value);
    }
}

package de.freese.ga.gene;

/**
 * @author Thomas Freese
 */
public class IntegerGene extends AbstractGene<Integer>
{
    /**
     * Erzeugt eine neue Instanz von {@link IntegerGene}.
     */
    public IntegerGene()
    {
        super();
    }

    /**
     * Erzeugt eine neue Instanz von {@link IntegerGene}.
     *
     * @param value Integer
     */
    public IntegerGene(final Integer value)
    {
        super();

        setValue(value);
    }
}

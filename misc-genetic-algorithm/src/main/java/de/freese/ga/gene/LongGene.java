package de.freese.ga.gene;

/**
 * @author Thomas Freese
 */
public class LongGene extends AbstractGene<Long>
{
    /**
     * Erzeugt eine neue Instanz von {@link LongGene}.
     */
    public LongGene()
    {
        super();
    }

    /**
     * Erzeugt eine neue Instanz von {@link LongGene}.
     *
     * @param value Long
     */
    public LongGene(final Long value)
    {
        super();

        setValue(value);
    }
}

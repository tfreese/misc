package de.freese.ga.gene;

/**
 * @author Thomas Freese
 */
public class BooleanGene extends AbstractGene<Boolean>
{
    /**
     * Erzeugt eine neue Instanz von {@link BooleanGene}.
     */
    public BooleanGene()
    {
        super();
    }

    /**
     * Erzeugt eine neue Instanz von {@link BooleanGene}.
     *
     * @param value Boolean
     */
    public BooleanGene(final Boolean value)
    {
        super();

        setValue(value);
    }
}
